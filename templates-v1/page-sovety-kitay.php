<script>
document.addEventListener('DOMContentLoaded', function() {
    // Код, перенесённый с имплантационной страницы для работы слайдеров и табов
    // Dots for comparison cards
    (() => {
        const track = document.getElementById('cardsTrack');
        const dotsRoot = document.getElementById('cardsDots');
        if (!track || !dotsRoot) return;
        const dots = Array.from(dotsRoot.querySelectorAll('.cards__dot'));
        const isMobile = () => window.matchMedia('(max-width: 680px)').matches;
        function updateDots() {
            if (!isMobile()) { dots.forEach(d => d.removeAttribute('aria-current')); return; }
            const i = Math.round(track.scrollLeft / track.clientWidth);
            dots.forEach((d, idx) => d.setAttribute('aria-current', idx === i ? 'true' : 'false'));
        }
        updateDots();
        dots.forEach((dot, idx) => dot.addEventListener('click', () => track.scrollTo({ left: idx * track.clientWidth, behavior: 'smooth' })));
        track.addEventListener('scroll', () => { requestAnimationFrame(updateDots); }, { passive: true });
        window.addEventListener('resize', updateDots);
    })();

    // Видео-отзывы слайдер (упрощённый перенос логики)
    const vrSlider = document.getElementById('videoReviewSlider');
    const vrTrack = vrSlider?.querySelector('.video-review-track');
    const vrSlides = vrSlider?.querySelectorAll('.slider-video-review-card') || [];
    const vrPrev = document.getElementById('prevVideoReview');
    const vrNext = document.getElementById('nextVideoReview');
    const vrDots = document.getElementById('videoReviewDots');
    let vrCurrentIndex = 0;
    function updateVideoDots() {
        if (!vrDots || window.innerWidth > 767) return;
        vrDots.innerHTML = '';
        for (let i = 0; i < vrSlides.length; i++) {
            const dot = document.createElement('span');
            if (i === vrCurrentIndex) dot.classList.add('active');
            dot.addEventListener('click', () => { vrCurrentIndex = i; updateVideoSlider(); });
            vrDots.appendChild(dot);
        }
    }
    function updateVideoSlider() {
        if (!vrTrack || !vrSlides.length) return;
        const slideWidth = vrSlides[0].offsetWidth + 20;
        vrTrack.style.transform = `translateX(-${vrCurrentIndex * slideWidth}px)`;
        updateVideoDots();
    }
    if (vrSlider && vrSlides.length > 0) {
        updateVideoSlider();
        vrPrev?.addEventListener('click', () => { if (vrCurrentIndex > 0) { vrCurrentIndex--; updateVideoSlider(); } });
        vrNext?.addEventListener('click', () => { if (vrCurrentIndex < vrSlides.length - 1) { vrCurrentIndex++; updateVideoSlider(); } });
        window.addEventListener('resize', updateVideoSlider);
    }

    // Доктора слайдер
    const doctorSlider = document.getElementById('doctorSlider');
    if (doctorSlider) {
        const doctorTrack = doctorSlider.querySelector('.doctor-slider-track');
        const doctorDotsContainer = document.getElementById('doctorDots');
        const doctorSlides = doctorSlider.querySelectorAll('.slider-doctor-card');
        const doctorPrev = document.getElementById('prevDoctor');
        const doctorNext = document.getElementById('nextDoctor');
        let doctorCurrentIndex = 0;
        function slidesToShow() { if (window.innerWidth >= 1024) return 3; if (window.innerWidth >= 768) return 2; return 1; }
        let show = slidesToShow();
        function updateDots() {
            doctorDotsContainer.innerHTML = '';
            for (let i = 0; i <= doctorSlides.length - show; i++) {
                const dot = document.createElement('span');
                if (i === doctorCurrentIndex) dot.classList.add('active');
                dot.addEventListener('click', () => { doctorCurrentIndex = i; update(); });
                doctorDotsContainer.appendChild(dot);
            }
        }
        function update() {
            const slideWidth = doctorSlides[0].offsetWidth + 30;
            doctorTrack.style.transform = `translateX(-${doctorCurrentIndex * slideWidth}px)`;
            updateDots();
        }
        doctorPrev?.addEventListener('click', () => { if (doctorCurrentIndex > 0) { doctorCurrentIndex--; update(); } });
        doctorNext?.addEventListener('click', () => { if (doctorCurrentIndex < doctorSlides.length - show) { doctorCurrentIndex++; update(); } });
        window.addEventListener('resize', () => { show = slidesToShow(); doctorCurrentIndex = 0; update(); });
        update();
    }
});
